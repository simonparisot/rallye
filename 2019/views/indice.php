<h2><i class="fas fa-info-circle"></i>Demande d'indice</h2>
<p>Nous vous invitons à demander des indices, la grande majorité des équipes le font ! Rester bloqué sur une énigme insoluble n'est jamais très sympatique, et comme on dit "un bon coup d'oeil vaut mieux qu'une mauvaise impasse" !</p>
<p>Chaque indice demandé coûte 25% des points associés à l'énigme.</p>
<p>Pour demander un indice, envoyez un mail à <b>theb2.rallye@gmail.com</b> en pensant bien à préciser :</p>
<ul>
	<li>Le numéro ou le nom de l'énigme en question</li>
	<li>Le nom de votre équipe</li>
	<li>Un maximum de détail sur votre avancement sur cette énigme : ce que vous avez déjà trouvé, vos pistes. Plus vous serez précis, plus notre indice sera adapté !</li>
</ul>